package com.callor.hello.nums;

public class HelloC {
	public static void main(String[] args) {
		// +,-,*,/ 기호는 (Operator)라고 부른다
		System.out.println(30 + 40); 
		System.out.println(30 - 40);
		System.out.println(30 * 40);
		System.out.println(30 / 40); // 순수 나눈 값(정수) 정수 나눗셈은 몫을 구한다
		System.out.println(30 % 40); // 나누고 남은 값 (%연산은 나머지를 구한다)
		
	}
}
